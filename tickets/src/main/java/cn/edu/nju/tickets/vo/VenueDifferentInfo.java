package cn.edu.nju.tickets.vo;

public class VenueDifferentInfo {
    private Integer id;
    private String email;
    private String venueName;
    private String city;
    private String detailPosition;
    private int seat;
    private String emailChange;
    private String venueNameChange;
    private String cityChange;
    private String detailPositionChange;
    private int seatChange;

    public VenueDifferentInfo() {
    }

    public Integer getId() {
        return id;
    }

    public void setId(Integer id) {
        this.id = id;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getVenueName() {
        return venueName;
    }

    public void setVenueName(String venueName) {
        this.venueName = venueName;
    }

    public String getCity() {
        return city;
    }

    public void setCity(String city) {
        this.city = city;
    }

    public String getDetailPosition() {
        return detailPosition;
    }

    public void setDetailPosition(String detailPosition) {
        this.detailPosition = detailPosition;
    }

    public int getSeat() {
        return seat;
    }

    public void setSeat(int seat) {
        this.seat = seat;
    }

    public String getEmailChange() {
        return emailChange;
    }

    public void setEmailChange(String emailChange) {
        this.emailChange = emailChange;
    }

    public String getVenueNameChange() {
        return venueNameChange;
    }

    public void setVenueNameChange(String venueNameChange) {
        this.venueNameChange = venueNameChange;
    }

    public String getCityChange() {
        return cityChange;
    }

    public void setCityChange(String cityChange) {
        this.cityChange = cityChange;
    }

    public String getDetailPositionChange() {
        return detailPositionChange;
    }

    public void setDetailPositionChange(String detailPositionChange) {
        this.detailPositionChange = detailPositionChange;
    }

    public int getSeatChange() {
        return seatChange;
    }

    public void setSeatChange(int seatChange) {
        this.seatChange = seatChange;
    }
}
